a = 10
b = 3

# pertambahan +
hasil = a + b
print(a, '+', b, '=', hasil)

# pengurangan -
hasil = a - b
print(a, '-', b, '=', hasil)

# perkalian *
hasil = a * b
print(a, '*', b, '=', hasil)

# pembagian /
hasil = a / b
print(a, '/', b, '=', hasil)

# sisa bagi %
hasil = a % b
print(a, '%', b, '=', hasil)

# sisa bagi **
hasil = a ** b
print(a, '**', b, '=', hasil)

# sisa bagi //
hasil = a // b
print(a, '//', b, '=', hasil)


